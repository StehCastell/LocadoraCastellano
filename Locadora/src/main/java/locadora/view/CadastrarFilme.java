    private void JBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSalvarActionPerformed
        try{
            if(!"".equals(JTNome.getText()) && !"".equals(JTAno.getText()))
            {
                String nome = JTNome.getText();
                int ano = Integer.parseInt(JTAno.getText());
                String genero = (String) JCGenero.getSelectedItem();                
                EGenero egenero = EGenero.valueOf(genero);

                Filme filme;
                
                FilmeDAO filmeDAO = new FilmeDAO();
                
                if(operacao == null){
                    filme = new Filme(0, nome, ano, egenero, true);
                    filmeDAO.salvar(filme);
                }
                else if(operacao.equals("Editar")){
                    filme = new Filme(filmeGlobal.getId(), nome, ano, egenero, filmeGlobal.getDisponivel());
                    filmeDAO.alterar(filme);
                }
                
                JOptionPane.showMessageDialog(null, "Salvo com Sucesso!!!");
                setVisible(false);
            }
            else {
                JOptionPane.showMessageDialog(null, "Campos Nome, ano e genero sao obrigat√≥rios.");
            }  
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_JBSalvarActionPerformed

