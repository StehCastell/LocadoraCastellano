    private void JBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBPesquisarActionPerformed
        if("".equals(JTCpf.getText())){            
            try {
                preencheTable();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ListCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            DefaultTableModel dftm = (DefaultTableModel) JTabela.getModel();

            while (dftm.getRowCount() > 0) {
                dftm.removeRow(0);
            }

            try {
                String cpf = JTCpf.getText();
                Cliente cliente = new ClienteDAO().buscarPeloCPF(cpf);
                listaClientes = new ArrayList<Cliente>();
                listaClientes.add(cliente);

                if(cliente == null || cliente.getId() == 0){
                    JOptionPane.showMessageDialog(null, "Nenhum cliente encontrado por esse cpf");
                }
                else{
                    for (Cliente c : listaClientes) {
                        String[] coluna = new String[5];
                        coluna[0] = c.getId() + "";
                        coluna[1] = c.getCpf();
                        coluna[2] = c.getNome();
                        coluna[3] = c.getEndereco();
                        dftm.addRow(coluna);
                    }
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao buscar o cliente: " + ex.getMessage());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ListCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_JBPesquisarActionPerformed

