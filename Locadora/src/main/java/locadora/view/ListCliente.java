    private void JBAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAtualizarActionPerformed
        final int idx = JTabela.getSelectedRow();

        if (idx < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente!!!");
        } else {
            CadastrarCliente tela = new CadastrarCliente();
            tela.preparedEdit(listaClientes.get(idx), "Editar");
            tela.setVisible(true);       
        }
    }//GEN-LAST:event_JBAtualizarActionPerformed

    private void JBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBExcluirActionPerformed
        final int idx = JTabela.getSelectedRow();

        if (idx < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente!!!");
        } else {
            Cliente cliente = listaClientes.get(idx);    
            ClienteDAO clienteDAO = new ClienteDAO();
            
            try {
                clienteDAO.apagar(cliente);
                listaClientes.remove(idx);
                JOptionPane.showMessageDialog(null, "Oper√ß√£o realizada com sucesso!!!");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }            
        }
    }//GEN-LAST:event_JBExcluirActionPerformed

    
    private void preencheTable() throws ClassNotFoundException {
        DefaultTableModel dftm = (DefaultTableModel) JTabela.getModel();

        while (dftm.getRowCount() > 0) {
            dftm.removeRow(0);
        }

        try {
            listaClientes = new ClienteDAO().buscarTodos();
            for (Cliente c : listaClientes) {
                String[] coluna = new String[5];
                coluna[0] = c.getId() + "";
                coluna[1] = c.getCpf();
                coluna[2] = c.getNome();
                coluna[3] = c.getEndereco();
                dftm.addRow(coluna);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar os clientes");
        }
    }
    
