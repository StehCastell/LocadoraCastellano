    private void JBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBPesquisarActionPerformed
        if("".equals(JTCliente.getText())){            
            try {
                preencheTable();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ListLocacao.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            DefaultTableModel dftm = (DefaultTableModel) JTabela.getModel();

            while (dftm.getRowCount() > 0) {
                dftm.removeRow(0);
            }

            String nome = JTCliente.getText();
            Locacao  locacao = new Locacao();
            
            try {
                listaLocacoes = new LocacaoDAO().buscarPeloCliente(nome);
            } catch (SQLException ex) {
                Logger.getLogger(ListLocacao.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ListLocacao.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            for (Locacao l : listaLocacoes) {
                String[] coluna = new String[6];
                coluna[0] = l.getId() + "";
                coluna[1] = l.getCliente().getNome();
                coluna[2] = l.getFilme().getNome();
                coluna[3] = l.getTipoValor().getDescricao();
                coluna[4] = l.getDataLocacao().toString();   
                coluna[5] = l.getDataDevolucao().toString();             
                dftm.addRow(coluna);
            }
        }
    }//GEN-LAST:event_JBPesquisarActionPerformed

    private void JBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBExcluirActionPerformed
        final int idx = JTabela.getSelectedRow();

        if (idx < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma locaçao!!!");
        } else {
            Locacao locacao = listaLocacoes.get(idx);    
            LocacaoDAO locacaoDAO = new LocacaoDAO();
            
            try {
                locacaoDAO.apagar(locacao);
                listaLocacoes.remove(idx);
                JOptionPane.showMessageDialog(null, "Operação realizada com sucesso!!!");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }            
        }
    }//GEN-LAST:event_JBExcluirActionPerformed

