    private void preencheTable() throws ClassNotFoundException {
        DefaultTableModel dftm = (DefaultTableModel) JTabela.getModel();

        while (dftm.getRowCount() > 0) {
            dftm.removeRow(0);
        }

        try {
            listaLocacoes = new LocacaoDAO().buscarTodos();
            for (Locacao l : listaLocacoes) {
                String[] coluna = new String[6];
                coluna[0] = l.getId() + "";
                coluna[1] = l.getCliente().getNome();
                coluna[2] = l.getFilme().getNome();
                coluna[3] = l.getTipoValor().getDescricao();
                coluna[4] = l.getDataLocacao().toString();   
                coluna[5] = l.getDataDevolucao().toString();             
                dftm.addRow(coluna);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar as locaçoes");
        }
    }
