    private ArrayList<Cliente> clientes;
    private ArrayList<Filme> filmes;
    private ArrayList<TipoValor> valores;
    
    private void preencheCombo() throws SQLException, ClassNotFoundException {        
        
        clientes = new ClienteDAO().buscarTodos();
        filmes = new FilmeDAO().buscarTodos();
        valores = new TipoValorDAO().buscarTodos();
        
        JCCliente.removeAllItems(); 
        JCFilme.removeAllItems();
        JCValor.removeAllItems();

        for(int i = 0; i < clientes.size(); i++) {
            JCCliente.addItem(clientes.get(i).getNome());
        }
        
        for(int i = 0; i < filmes.size(); i++) {
            JCFilme.addItem(filmes.get(i).getNome());
        }
        
        for(int i = 0; i < valores.size(); i++) {
            JCValor.addItem(valores.get(i).getDescricao() + " - R$ " + valores.get(i).getValor());
        }
    }
    
