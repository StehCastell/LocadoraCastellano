    private void JBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSalvarActionPerformed
        try{
            if(!"".equals(JTDataLocacao.getText()) && !"".equals(JTDataDevolucao.getText()))
            {
                int idxCliente = (int) JCCliente.getSelectedIndex();
                int idxFilme = (int) JCFilme.getSelectedIndex();
                // Date dataLocacao = new Date(JTDataLocacao.getText());
                //Date dataDevolucao = new Date(JTDataLocacao.getText());
                SimpleDateFormat stf = new SimpleDateFormat("dd/MM/yyyy");
                
                Date dataLocacao = stf.parse(JTDataLocacao.getText());
                Date dataDevolucao = stf.parse(JTDataDevolucao.getText());
                int idxTipoValor = (int) JCValor.getSelectedIndex(); 

                Cliente cliente = clientes.get(idxCliente);
                Filme filme = filmes.get(idxFilme);
                TipoValor tipoValor = valores.get(idxTipoValor);
                
                Locacao locacao;
                
                LocacaoDAO locacaoDAO = new LocacaoDAO();
                
                if(operacao == null){
                    locacao = new Locacao(0, cliente, filme, tipoValor, dataLocacao, dataDevolucao);
                    locacaoDAO.salvar(locacao);
                }
                else if(operacao.equals("Editar")){
                    locacao = new Locacao(locacaoGlobal.getId(), cliente, filme, tipoValor, dataLocacao, dataDevolucao);
                    locacaoDAO.alterar(locacao);
                }
                
                JOptionPane.showMessageDialog(null, "Salvo com Sucesso!!!");
                setVisible(false);
            }
            else {
                JOptionPane.showMessageDialog(null, "Campos data de locaçao e data de devoluçao sao obrigatórios.");
            }  
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_JBSalvarActionPerformed

