    private void JBAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAtualizarActionPerformed
        final int idx = JTabela.getSelectedRow();

        if (idx < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um valor!!!");
        } else {
            CadastrarValores tela = new CadastrarValores();
            tela.preparedEdit(listaValores.get(idx), "Editar");
            tela.setVisible(true);       
        }
    }//GEN-LAST:event_JBAtualizarActionPerformed

    private void JBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBPesquisarActionPerformed
        if("".equals(JTDescricao.getText())){            
            try {
                preencheTable();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ListValores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            DefaultTableModel dftm = (DefaultTableModel) JTabela.getModel();

            while (dftm.getRowCount() > 0) {
                dftm.removeRow(0);
            }

            try {
                String descricao = JTDescricao.getText();
                TipoValor tipoValor = new TipoValorDAO().buscarPelaDescricao(descricao);
                listaValores = new ArrayList<TipoValor>();
                listaValores.add(tipoValor);

                if(tipoValor == null || tipoValor.getId() == 0){
                    JOptionPane.showMessageDialog(null, "Nenhum valor encontrado por essa descriçao");
                }
                else{
                    for (TipoValor tv : listaValores) {
                        String[] coluna = new String[5];
                        coluna[0] = tv.getId() + "";
                        coluna[1] = tv.getDescricao();
                        coluna[2] = tv.getValor() + "";
                        dftm.addRow(coluna);
                    }
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao buscar o valor: " + ex.getMessage());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ListCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_JBPesquisarActionPerformed

    private void JBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBExcluirActionPerformed
        final int idx = JTabela.getSelectedRow();

        if (idx < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um valor!!!");
        } else {
            TipoValor tipoValor = listaValores.get(idx);    
            TipoValorDAO tipoValorDAO = new TipoValorDAO();
            
            try {
                tipoValorDAO.apagar(tipoValor);
                listaValores.remove(idx);
                JOptionPane.showMessageDialog(null, "Operção realizada com sucesso!!!");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }            
        }
    }//GEN-LAST:event_JBExcluirActionPerformed

