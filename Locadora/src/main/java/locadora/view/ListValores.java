    private void preencheTable() throws ClassNotFoundException {
        DefaultTableModel dftm = (DefaultTableModel) JTabela.getModel();

        while (dftm.getRowCount() > 0) {
            dftm.removeRow(0);
        }

        try {
            listaValores = new TipoValorDAO().buscarTodos();
            for (TipoValor c : listaValores) {
                String[] coluna = new String[5];
                coluna[0] = c.getId() + "";
                coluna[1] = c.getDescricao();
                coluna[2] = c.getValor() + "";
                dftm.addRow(coluna);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar os valores");
        }
    }
